def resolve_hq_and_office(address: str, fallback_bjd: str = "", _assign_office_func=None) -> tuple:
    """주소 텍스트를 분석하여 대응하는 (본부, 관할지사)를 반환합니다."""
    # 주소 정제
    addr_clean = (address or "").strip()
    if not addr_clean and fallback_bjd:
        try:
            from bjd_mapper import get_bjd_name_str
            addr_clean = get_bjd_name_str(fallback_bjd)
        except:
            pass
            
    if not addr_clean:
        return "본부확인요망", "관할지사확인요망"

    hq = "본부확인요망"
    office = "관할지사확인요망"

    # 본부 검색
    # 가장 구체적인 지역명부터 매칭을 시도하는 것이 좋습니다.
    for hq_name, keywords in KEPCO_REGION_MAP:
        for kw in keywords:
            # 주소 내에 시도/구군 이름이 독립된 단어로 등장하는지 확인
            if kw in addr_clean:
                hq = hq_name
                break
        if hq != "본부확인요망":
            break
            
    # 본부를 찾지 못했으면, 일단 주소를 관할지사로 사용
    if hq == "본부확인요망":
        # 혹시 몰라 한 번 더 넓게 검사
        if "대구" in addr_clean: hq = "대구본부"
        elif "서울" in addr_clean: hq = "서울본부"
        elif "경기" in addr_clean: hq = "경기본부"
        elif "인천" in addr_clean: hq = "인천본부"
    
    # 지사 할당
    if hq == "대구본부" or hq == "경북본부":
        if _assign_office_func:
            office = _assign_office_func("", addr_clean)
        else:
            parts = addr_clean.split()
            office = parts[1] if len(parts) >= 2 else addr_clean
    else:
        # 타 본부는 사용자가 제공한 HQ_OFFICE_MAP 기반으로 매핑 시도
        offices = HQ_OFFICE_MAP.get(hq, [])
        found_office = None
        for off in offices:
            if off.startswith("직할("):
                # 예: "직할(중구·종로구)" -> 중구, 종로구 추출
                match = re.search(r'\((.*?)\)', off)
                if match:
                    keywords = [k.strip() for k in match.group(1).replace('·', ',').split(',')]
                    if any(k in addr_clean for k in keywords):
                        found_office = "직할"
                        break
            else:
                # 예: "용산지사" -> "용산"
                core_name = off.replace("지사", "")
                if core_name in addr_clean:
                    found_office = off
                    break
        
        if found_office:
            office = found_office
        else:
            # 매핑 실패 시 기존 폴백 (주소의 두번째 단어 사용)
            parts = addr_clean.split()
            if len(parts) >= 2:
                office = parts[1]
            else:
                office = addr_clean
            
    return hq, office
